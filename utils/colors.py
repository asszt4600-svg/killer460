# color.py
class Color:
    RED    = "\033[91m"   # حق الاخطاء او الايرورز
    GREEN  = "\033[92m"   # اذا كيلر سوا الامر صح 
    YELLOW = "\033[93m"   # للانفورميشن
    BLUE   = "\033[94m"   # للعنواين
    CYAN   = "\033[96m"   # للفلسفه او التفاصيل 
    WHITE  = "\033[97m"   # الكلام العادي او اللون العادي
    RESET  = "\033[0m"    # ايقاف اللون او يوقف الالوان ومنه فايده بالنهايه او مهمه



# انشاء اختصار عشان نحط اللوان
def print_color(text, color):
    print(f"{color}{text}{Color.RESET}")